int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;

    int l = 0;

    int ans = 0;

    for (int r = 1; r < n; r++) {
        while (l < r) {
            cout << "? " << l + 1 << " " << r + 1 << endl;
            string resp;
            cin >> resp;
            if (resp == "Yes") {
                ans += r - l;
                break;
            } else {
                l++;
            }
        }
    }

    cout << "! " << ans << endl;

    return 0;
}
